namespace TimeCalculator
{
    /// <summary>
    /// Types of time units.
    /// </summary>
    public enum ETimeUnit
    {
        /// <summary>
        /// Represents a 마이크로초.
        /// </summary>
        MicroSecond,

        /// <summary>
        /// Represents a 밀리초.
        /// </summary>
        MilliSecond,

        /// <summary>
        /// Represents a 초.
        /// </summary>
        Second,

        /// <summary>
        /// Represents a 분.
        /// </summary>
        Minute,

        /// <summary>
        /// Represents a 시간.
        /// </summary>
        Hour,

        /// <summary>
        /// Represents a 일.
        /// </summary>
        Day,

        /// <summary>
        /// Represents a 주.
        /// </summary>
        Week,

        /// <summary>
        /// Represents a 년.
        /// </summary>
        Year
    }
}
